package org.types;

public class PrimitiveTypesString {
    /*
     * In Java :
     * -Single characters must be enclosed in single quotes (''), e.g., 'A'.
     * -Multiple characters or text (a sequence of characters) must be enclosed in double quotes (" "), e.g .,"Maxwell" .
     *
     * Important:
     * 'char' is a primitive type (it stores a single character).
     * 'String' is not a primitive type. It is a reference type provided by Java
     * to represent a sequence of characters.
     *
     * Both are shown here together just to make the difference between
     * single quotes and double quotes clear.
     *
     * The 'char' type is used to store a single character
     * The 'String' type is used to store a sequence of characters
     */
    char letter= 'A';
    String firstName= "Maxwell";
}
